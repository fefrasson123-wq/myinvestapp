export const getStockByTicker = (ticker: string): StockAsset | undefined => {
  return stocksList.find(s => s.ticker.toUpperCase() === ticker.toUpperCase());
};

export const searchStocks = (query: string): StockAsset[] => {
  const normalizedQuery = query.toLowerCase();
  return stocksList.filter(s => 
    s.ticker.toLowerCase().includes(normalizedQuery) || 
    s.name.toLowerCase().includes(normalizedQuery)
  );
};

export const searchFiis = (query: string): StockAsset[] => {
  const normalizedQuery = query.toLowerCase();
  return fiiList.filter(f => 
    f.ticker.toLowerCase().includes(normalizedQuery) || 
    f.name.toLowerCase().includes(normalizedQuery)
  );
};

